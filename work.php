		<h1 id="welcome">Work of Ada Lovelace!</h1>
		<p class="bio_par">
		<img id="biopic" alt="" src="http://www.ieeeghn.org/wiki/images/8/87/Ada_Lovelace.jpg"></img> 
		Throughout her life, Ada was strongly interested in scientific developments 
		and fads of the day, including phrenology and mesmerism. Even after her 
		famous work with Babbage, Ada continued to work on other projects. In 1844, 
		she commented to a friend Woronzow Greig about her desire to create a mathematical 
		model for how the brain gives rise to thoughts and nerves to feelings ("a 
		calculus of the nervous system"). She never achieved this, however. In part, 
		her interest in the brain came from a long-running preoccupation, inherited 
		from her mother, about her "potential" madness. As part of her research 
		into this project, she visited electrical engineer Andrew Crosse in 1844 
		to learn how to carry out electrical experiments. In the same year, she 
		wrote a review of a paper by Baron Karl von Reichenbach, Researches on Magnetism, 
		but this was not published and does not appear to have progressed past the 
		first draft. In 1851, the year before her cancer struck, she wrote to her 
		mother mentioning certain productions she was working on regarding the relation 
		of maths and music.</p>
		<p class="bio_par">Lovelace first met Charles Babbage in June 1833, through 
		their mutual friend Mary Somerville. Later that month, Babbage invited Lovelace 
		to see the prototype for his Difference Engine. She became fascinated with 
		the machine and used her relationship with Somerville to visit Babbage as 
		often as she could. Babbage was impressed by Lovelace's intellect and analytic 
		skills. He called her <i>The Enchantress of Numbers</i>. In 1843 he wrote 
		of her:</p>
		<p class="bio_par"><i>Forget this world and all its troubles and if possible 
		its multitudinous Charlatans—every thing in short but the Enchantress of 
		Numbers.</i></p>
		<p class="bio_par">During a nine-month period in 1842–43, Ada translated 
		Italian mathematician Luigi Menabrea';s memoir on Babbage's newest proposed 
		machine, the Analytical Engine. With the article, she appended a set of 
		notes. Explaining the Analytical Engine's function was a difficult task, 
		as even other scientists did not really grasp the concept and the British 
		establishment was uninterested in it. Ada's notes even had to explain how 
		the Engine differed from the original Difference Engine.</p>
		<p class="bio_par">The notes are longer than the memoir itself and include 
		(in Section G), in complete detail, a method for calculating a sequence 
		of Bernoulli numbers with the Engine, which would have run correctly had 
		the Analytical Engine been built (only his Difference Engine has been built, 
		completed in London in 2002).</p>
		<p class="bio_par">Based on this work, Lovelace is now widely credited with 
		being the first computer programmer and her method is recognised as the 
		world&#39;s first computer program. Her work was well received at the time; 
		Michael Faraday described himself as a fan of her writing.</p>
		<p class="bio_par">Lovelace and Babbage had a minor falling out when the 
		papers were published, when he tried to leave his own statement (a criticism 
		of the government's treatment of his Engine) as an unsigned preface—which 
		would imply that she had written that also. When Taylor's <i>Scientific 
		Memoirs</i> ruled that the statement should be signed, Babbage wrote to 
		Ada asking her to withdraw the paper. This was the first that she knew he 
		was leaving it unsigned, and she wrote back refusing to withdraw the paper. 
		Historian Benjamin Woolley theorised that, "His actions suggested he had 
		so enthusiastically sought Ada's involvement, and so happily indulged her 
		... because of her 'celebrated name'. Their friendship recovered, and they 
		continued to correspond. On 12 August 1851, when she was dying of cancer, 
		Ada wrote to him asking him to be her executor, though this letter did not 
		give him the necessary legal authority. Part of the terrace at Worthy Manor 
		was known as <i>Philosopher's Walk</i>, as it was there that Ada and Babbage 
		were reputed to have walked while discussing mathematical principles.
