		<h1 id="welcome">Biography of Ada Lovelace!</h1>
		<p class="bio_par">
		<img id="biopic" alt="" src="http://www.ieeeghn.org/wiki/images/8/87/Ada_Lovelace.jpg"></img><strong>Augusta 
		Ada King</strong>, Countess of Lovelace (10 December 1815 – 27 November 
		1852), born Augusta Ada Byron and now commonly known as Ada Lovelace, was 
		an English mathematician and writer chiefly known for her work on Charles 
		Babbage's early mechanical general-purpose computer, the Analytical Engine. 
		Her notes on the engine include what is recognised as the first algorithm 
		intended to be carried out by a machine. Because of this, she is often described 
		as the world's first computer programmer.</p>
		<p class="bio_par">Lovelace was born 10 December 1815 as the only child 
		of the poet Lord Byron and his wife Anne Isabella Byron. All Byron's other 
		children were born out of wedlock to other women. Byron separated from his 
		wife a month after Ada was born and left England forever four months later, 
		eventually dying of disease in the Greek War of Independence when Ada was 
		eight years old. Ada's mother remained bitter at Lord Byron and promoted 
		Ada's interest in mathematics and logic in an effort to prevent her from 
		developing what she saw as the insanity seen in her father, but Ada remained 
		interested in him despite this (and was, upon her eventual death, buried 
		next to him at her request).</p>
		<p class="bio_par">Ada described her approach as "poetical science" and 
		herself as an "Analyst (&amp; Metaphysician)". As a young adult, her mathematical 
		talents led her to an ongoing working relationship and friendship with fellow 
		British mathematician Charles Babbage, and in particular Babbage's work 
		on the Analytical Engine. Between 1842 and 1843, she translated an article 
		by Italian military engineer Luigi Menabrea on the engine, which she supplemented 
		with an elaborate set of notes of her own, simply called Notes. These notes 
		contain what many consider to be the first computer program—that is, an 
		algorithm designed to be carried out by a machine. Lovelace's notes are 
		important in the early history of computers. She also developed a vision 
		on the capability of computers to go beyond mere calculating or number-crunching 
		while others, including Babbage himself, focused only on those capabilities. 
		Her mind-set of "poetical science" led her to ask basic questions about 
		the Analytical Engine (as shown in her notes) examining how individuals 
		and society relate to technology as a collaborative tool.</p>
